package year2021.month12.no91;

public class DecodeWays {

    public static void main(String[] args) {
        String s1 = "12";
        String s2 = "226";
        String s3 = "0";
        String s4 = "06";
        String s5 = "106";
        String s6 = "006";
        System.out.println(numDecodings(s1));
        System.out.println(numDecodings(s2));
        System.out.println(numDecodings(s3));
        System.out.println(numDecodings(s4));
        System.out.println(numDecodings(s5));
        System.out.println(numDecodings(s6));
    }

    public static int numDecodings(String s) {
        /*
         * DP
         * 状态定义：
         * dp[i] 表示 s[0,i) 所能转换成不同字母的数量
         * 状态转移方程：
         * if (s[i] != '0')
         *   dp[i] = dp[i-1]
         * if s[i-1] != '0' && (s[i-1] - '0') * 10 + (s[i] - '0') <= 26
         *   dp[i] += dp[i-2]
         * */
        int n = s.length();
        int[] dp = new int[n + 1];
        dp[0] = 1;
        for (int i = 1; i <= n; i++) {
            if (s.charAt(i - 1) != '0') {
                dp[i] = dp[i - 1];
            }
            if (i > 1 && s.charAt(i - 2) != '0' && ((s.charAt(i - 2) - '0') * 10 + (s.charAt(i - 1) - '0') <= 26)) {
                dp[i] += dp[i - 2];
            }
        }
        return dp[n];
    }

}

